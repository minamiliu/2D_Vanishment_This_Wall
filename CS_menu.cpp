/*******************************************************************************
* グローバル変数
*******************************************************************************/
bool g_playerReady;

/*******************************************************************************
関数名:	void InitTitle(void)
引数:	なし
戻り値:	なし
説明:	ゲームタイトルの初期化処理
*******************************************************************************/
void InitCSMenu(void)
{
	InitCS_BG();
	InitCS_Select();
	InitCS_CharaShow();
	g_playerReady = false;
}

/*******************************************************************************
関数名:	void UpdateTitle(void)
引数:	なし
戻り値:	なし
説明:	ゲームタイトルの更新関数
*******************************************************************************/
void UpdateCSMenu(void)
{
	if(!g_playerReady)
	{
		UpdateCS_Select();
		Update_CharaShow();
	}
}

/*******************************************************************************
関数名:	void DrawTitle(void)
引数:	なし
戻り値:	なし
説明:	ゲームタイトルの描画関数
*******************************************************************************/
void DrawCSMenu(void)
{
	DrawCS_BG();
	DrawCS_CharaShow();
	DrawCS_Select();
}
/*******************************************************************************
関数名:	void UninitTitle(void)
引数:	なし
戻り値:	なし
説明:	ゲームタイトルの開放関数
*******************************************************************************/
void UninitCSMenu(void)
{
	UninitCS_BG();
	UninitCS_Select();
	Uninit_CharaShow();
}

void AllPlayerLock(void)
{
	g_playerReady = true;
}

